import { useState, useEffect } from "react";
import Head from "next/head";
import Link from "next/link";
import { API, Storage } from "aws-amplify";
import { listPosts } from "../src/graphql/queries";
import PostImage from "../components/FrontPage/PostImage";

export default function Home() {
  const [posts, setPosts] = useState([]);
  useEffect(() => {
    fetchPosts();
  }, []);
  async function fetchPosts() {
    const postData = await API.graphql({
      query: listPosts,
    });
    const { items } = postData.data.listPosts;
    // Fetch images from S3 for posts that contain a cover image
    const postsWithImages = await Promise.all(
      items.map(async (post) => {
        if (post.postImage) {
          post.postImage = await Storage.get(post.postImage);
        }
        return post;
      })
    );
    setPosts(postsWithImages);
  }
  return (
    <div>
      <Head>
        <title>Big D TBD</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <h1 className="text-3xl font-semibold tracking-wide mt-6 mb-8">
        Stories by Danno
      </h1>
      {posts.map((post, index) => {
        return (
          <Link key={index} href={`/posts/${post.id}`} passHref>
            <div className="my-6 pb-6 border-b border-gray-300">
              <div className="cursor-pointer mt-2">
                <h2 className="text-xl font-semibold">
                  <PostImage
                    label={post.postEmojiTitle}
                    emoji={post.postEmoji}
                  />
                  {post.title}
                </h2>
                <p className="text-gray-500 mt-2">Author: {post.username}</p>
              </div>
            </div>
          </Link>
        );
      })}

      <footer>
        <span></span>
      </footer>
    </div>
  );
}
